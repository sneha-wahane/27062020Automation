package variableAndDataTypes;

public class ArithmeticOperators {

	public static void main(String[] args) {
		//+, -, *, /,%
		int a=10;
		int b=20;
		//+ operator
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a*b);
		System.out.println(b%a);//use for remainder
		System.out.println(b/a);
	}

}

package loops;

public class WhileLoop {

	public static void main(String[] args) {
		// Initialization
		int i=0;
		while(i<=10)//condition
		{
			System.out.println(i);
			i++;//Increment or decrement
		}
		int j=10;
		while(j>=0)//condition
		{
			System.out.println(j);
			j--;//Increment or decrement
		}
	}

}

package loops;

public class NestedLoop {

	public static void main(String[] args) {
		for(int i=0; i<=10;i++) //i=rows
		{
			for(int j=0;j<=10;j++)//j=columns
			{
				//System.out.println("rows no.="+i+" and column number="+j);
				System.out.print(j+" ");
			}
			System.out.println();
		}
		

	}

}
